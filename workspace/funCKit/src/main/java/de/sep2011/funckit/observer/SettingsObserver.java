package de.sep2011.funckit.observer;

import de.sep2011.funckit.model.sessionmodel.Settings;

/**
 * Classes Implementing this usually want to observe a Settings Object. See
 * {@link FunckitObservable} for details how to add something as Observer.
 */
public interface SettingsObserver {

    /**
     * Notifies the Observer that the Settings Object changed in some way.
     * 
     * @param source
     *            The source Settings Object.
     * @param i
     *            a Info Object with additional Information
     */
    public void settingsChanged(Settings source, SettingsInfo i);
}
